#ifndef __DAC__
#define __DAC__

#include <inttypes.h>

namespace AVRSound {

/** =========================================================================
 * @brief   DAC毎の処理の違いを吸収し統一なインターフェースを提供する。
 * ========================================================================== */
class DAC {
public:
    enum LR{
        LR_MIN = 0,
        L_CH = LR_MIN,
        R_CH,
        LR_MAX
    };

    /** =========================================================================
     * @brief   初期化処理
     * ========================================================================== */
    void Initialize();

    /** =========================================================================
     * @brief   データの出力
     * @param[in] value 出力する値
     * @param[in] lr    出力するチャンネル
     * ========================================================================== */
    void Output(uint8_t value, LR lr = LR::L_CH);

    /** =========================================================================
     * @brief   Lチャンネルにデータを出力する
     * @param[in] value 出力する値
     * ========================================================================== */
    void OutputL(uint8_t value);

    /** =========================================================================
     * @brief   Rチャンネルにデータを出力する
     * @param[in] value 出力する値
     * ========================================================================== */
    void OutputR(uint8_t value);

};
}

#endif